# Adição (+)
x = 2 + 2
print(x)

# Subtração (-)
x = 2 - 2
print(x)

# Multiplicação (*)
x = 2 * 3
print(x)

# Divisão (/) (Se utilizar o / duas vezes resulta em apenas numeros inteiros)
x = int(10 / 3)
y = 10 // 3
print(float(x))
print(int(y))

# Exponenciação (**)
x = 2 ** 3
print(x)

# Resto da divisão (%)
x = 10 % 3
y = 10 % 2
print(x)
print(y)

# -------------------------------------- #

# Expressões Matemáticas
x = 5 * 2 + 3 * 2
print(x)
# é igual a isso =
y = (5 * 2) + (3 * 2)
print(y)
# é diferente disso <>
z = 5 * (2 + 3) * 2
print(z)

# -------------------------------------- #

# variável

x = 5 * 2
print(x)
y = x + 3 * 2
print(y)
